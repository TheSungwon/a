package javaSE1019;

public class CompareOperatorExample {
	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 10;
		
		boolean a = (num1 == num2);
		boolean b = (num1 != num2);
		boolean c = (num1 <= num2);
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		
		
		char char1 = 'A';
		char char2 = 'B';
		
		boolean d = (char1 < char2);
		System.out.println(d);
	}

}
