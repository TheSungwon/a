package javaSE1015;

public class variable1 {
  public static void main(String[] args) {
	  int value = 10;
	  //10을 변수 value의 초기값으로 저장
	  
	  int result = value + 10;
	
	  System.out.println(result);
	  //변수 value 값을 읽고 10을 더하는 산술 연산을 수행
	  //연산의 결과값을 변수 result의 초기값으로 저장
	  
	  //변수 result 값을 읽고 콘솔에 입력
  }
}
