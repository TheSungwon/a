public class ifExample {
	public static void main(String[] args) {
		
		int score = 93;
		
		if(score >= 90) {
			System.out.println("90보다 큼");
			System.out.println("A등급");
			
		}
		
		if(score < 90)
			System.out.println("90보다 작음");
	
		
		System.out.println("B등급"); //if문과 상관없는 실해문
		
	}

}
