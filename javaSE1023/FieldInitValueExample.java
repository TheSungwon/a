package javaSE1023;

public class FieldInitValueExample {
	public static void main(String[] args) {
		FieldInitValue a = new FieldInitValue();
		
		System.out.println(a.byteF);
		System.out.println(a.intF);
		System.out.println(a.shortF);
		System.out.println(a.longF);
		System.out.println();
		System.out.println(a.booleanF);
		System.out.println("char"+a.charf);
		System.out.println();
		System.out.println(a.floatF);
		System.out.println(a.doublef);
		System.out.println();
		System.out.println(a.iintF);
		System.out.println(a.StringF);
	}

}
