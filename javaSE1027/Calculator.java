package javaSE1027;

public class Calculator {
	
	//Method
	
	void powerON() {
		System.out.println("ON");
	}
	
	int plus(int x, int y) {
		int result = x + y;
		return result;
	}
	
	double divide(int x, int y) {
		double result = (double)x / (double)y;
		return result;
	}
	
	void powerOff() {
		System.out.println("OFF");
	}

}
